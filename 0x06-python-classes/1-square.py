#!/usr/bin/python3

class Square:
    """
    A class that defines a square.

    Attributes:
        __size (int): Private instance attribute
                        representing the size of the square.

    Methods:
        None
    """

    def __init__(self, size):
        """
        Initializes a square with a specified size.

        Args:
            size (int): Size of the square.
        """
        self.__size = size
